

double averageAge;

averageAge = (43 + 22 + 63) / 3; // adding integers will lead to an integer result, not a double
                                 // integers can fit into doubles, but no the other way around, integers are simpler than a decimal number
                                 // you would need to append the decimal off of a double to put it into an integer, loss of precision

averageAge = 2 / 3.0;

